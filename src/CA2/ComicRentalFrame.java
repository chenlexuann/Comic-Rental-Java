    private void btnEarningStatsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEarningStatsActionPerformed
        // TODO add your handling code here:
        double Rental = 0;
        for (int i = 0; i < RS.getRenteeSize(); i++) {
            for (int y = 0; y < RS.getRentee(i).getComicsLoaned().length; y++) {
                Comic comicsLoaned = RS.SearchComics(RS.getRentee(i).getComicsLoaned()[y]);
                Rental += comicsLoaned.RentalFeePD();
            }
        }
        txtAreaMsg.setText(RS.DisplayEarnings(Rental));
    }//GEN-LAST:event_btnEarningStatsActionPerformed

