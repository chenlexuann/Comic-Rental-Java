    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // search either comic or rentee
        if (rbComic.isSelected()) {
            Comic comic = RS.SearchComics(txtSearch.getText());
            if (comic != null) {
                ComicIndex = RS.getComicIndex(txtSearch.getText());
                lblComic.setText("Comic " + (ComicIndex + 1) + " of " + RS.getComicSize());
                txtISBN.setText(comic.getISBN13Number());
                txtTitle.setText(comic.getTitleName());
                txtRental.setText("$" + String.format("%.2f", comic.RentalFeePD()));
                txtDeposit.setText("$" + String.format("%.2f", comic.DepositFee()));
                txtAreaInfo.setText(comic.AddInfo());
                txtAreaMsg.setText("Found and displayed item with the ISBN-13: " + txtSearch.getText());
            } else {
                txtAreaMsg.setText("No such ISBN-13 \"" + txtSearch.getText() + "\" in the data!");
            }

        } else {
            Rentee rentee = RS.SearchRentees(txtSearch.getText());
            if (rentee != null) {
                RenteeIndex = RS.getRenteeIndex(txtSearch.getText());
                lblRentee.setText("Rentee " + (RenteeIndex + 1) + " of " + RS.getRenteeSize());
                txtMemberID.setText(rentee.getMemberID());
                txtName.setText(rentee.getName());
                dtm.setRowCount(0);
                for (int i = 0; i < rentee.getComicsLoaned().length; i++) {
                    Comic comicsLoaned = RS.SearchComics(rentee.getComicsLoaned()[i]);
                    String ISBN = comicsLoaned.getISBN13Number();
                    String Title = comicsLoaned.getTitleName();
                    String Rental = "$" + String.format("%.2f", comicsLoaned.RentalFeePD());
                    String Deposit = "$" + String.format("%.2f", comicsLoaned.DepositFee());
                    Object[] objs = {ISBN, Title, Rental, Deposit};
                    dtm.addRow(objs);
                }
                txtAreaMsg.setText("Found and displayed item with the Member ID: " + txtSearch.getText());
            } else {
                txtAreaMsg.setText("No such Member ID \"" + txtSearch.getText() + "\" in the data!");
            }
        }
        txtSearch.setText("");
    }//GEN-LAST:event_btnSearchActionPerformed

